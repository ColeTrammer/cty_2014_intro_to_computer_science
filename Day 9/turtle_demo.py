import turtle


def main():
    window = turtle.Screen()
    Chris = turtle.Turtle()
    Chris.forward(150)
    Chris.left(90)
    Chris.forward(150)
    Chris.left(90)
    Chris.forward(150)
    Chris.left(90)
    Chris.forward(150)
    Chris.left(90)
    window.exitonclick()

if __name__ == "__main__":
    main()

